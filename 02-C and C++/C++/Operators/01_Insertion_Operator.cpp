#include <iostream>
using namespace std;

int main() {

    // Print a Prompt Message
    cout << "Hello World!";

   // Print On New Line
    cout << "Hello World!" << "\n";
    cout << "I am learning C++";
  
  
    // Printing on NewLine using endl
    cout << "Hello World!" << endl;
    cout << "I am learning C++";

    // Printing Values 
    int a=10;
    int b=20;
  
    cout << "Value of a is " << a << endl;
    cout << "Value of b is " << b << endl;
    return 0;
}
